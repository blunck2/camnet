package campublisher.engine;

public class ImageRetrievalException extends Exception {
	public ImageRetrievalException() { super(); }
	public ImageRetrievalException(String message) { super(message); }
	public ImageRetrievalException(String message, Throwable cause) { super(message, cause); }
	public ImageRetrievalException(Throwable cause) { super(cause); }
}